async function editIncident(id){

    const newStatus =
    prompt(
    "Enter Status:\nPending\nIn Progress\nResolved"
    );

    if(!newStatus) return;

    await fetch(
    `${API_URL}/${id}`,
    {
        method:"PUT",

        headers:{
            "Content-Type":
            "application/json"
        },

        body:JSON.stringify({
            status:newStatus
        })
    });

    loadIncidents();
}


document
.getElementById(
"searchInput"
)
.addEventListener(
"keyup",
function(){

    const value =
    this.value.toLowerCase();

    const rows =
    document.querySelectorAll(
    "#incidentTable tr"
    );

    rows.forEach(row=>{

        if(
            row.innerText
            .toLowerCase()
            .includes(value)
        ){

            row.style.display = "";

        }
        else{

            row.style.display = "none";

        }

    });

});


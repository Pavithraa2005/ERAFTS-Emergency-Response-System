function renderTable(data){

    incidentTable.innerHTML = "";

    data.forEach(incident => {

        const badgeClass =
(incident.severity || "Low").toLowerCase();

        incidentTable.innerHTML += `

        <tr>

            <td>
                ${incident.id}
            </td>

            <td>
                ${getIcon(
                incident.emergency_type
                )}

                ${incident.emergency_type}
            </td>

            <td>
                ${incident.location_zone}
            </td>

            <td>

                <span class="
                badge
                ${badgeClass}
                ">

               ${incident.severity || "Low"}

                </span>

            </td>

           <td>
    <span class="
    status
    ${incident.status
    .replace(/\s/g,'')
    .toLowerCase()}
    ">
    ${incident.status}
    </span>
</td> 

<td>

${
role === "admin"
?

`<button
onclick="editIncident(${incident.id})"
class="edit-btn">

✏ Edit

</button>

<button
onclick="deleteIncident(${incident.id})"
class="delete-btn">

🗑 Delete

</button>`

:

""

}

</td>

        </tr>

        `;

    });

}


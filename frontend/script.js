function renderIncidentChart(data){

    const medical =
    data.filter(
    i=>i.emergency_type==="Medical"
    ).length;

    const fire =
    data.filter(
    i=>i.emergency_type==="Fire"
    ).length;

    const flood =
    data.filter(
    i=>i.emergency_type==="Flood"
    ).length;

    const accident =
    data.filter(
    i=>i.emergency_type==="Accident"
    ).length;

    const ctx =
    document.getElementById(
    "incidentChart"
    );

    if(incidentChart){

        incidentChart.destroy();

    }

    incidentChart =
    new Chart(ctx,{

        type:"doughnut",

        data:{

            labels:[
                "Medical",
                "Fire",
                "Flood",
                "Accident"
            ],

            datasets:[{

                data:[
                    medical,
                    fire,
                    flood,
                    accident
                ],

                backgroundColor:[

                    "#3b82f6",
                    "#ef4444",
                    "#06b6d4",
                    "#f59e0b"

                ]

            }]

        }

    });

}


form.addEventListener(
"submit",
async (e)=>{

    e.preventDefault();

    const emergency_type =
    document.getElementById(
    "emergency_type"
    ).value;

    const location_zone =
    document.getElementById(
    "location_zone"
    ).value;

    const severity =
    document.getElementById(
    "severity"
    ).value;

    const status =
document.getElementById(
"status"
).value;

    const response =
    await fetch(API_URL,{

        method:"POST",

        headers:{
            "Content-Type":
            "application/json"
        },

        body:JSON.stringify({

            emergency_type,
            location_zone,
            severity,
            status

        })

    });

    if(response.ok){

        addActivity(
        `${emergency_type}
        incident reported`
        );

        form.reset();

        loadIncidents();

    }

});

